// Defines all resource types available in the game.

namespace GalacticEmpire.Core
{
    /// <summary>All resource types in the empire economy.</summary>
    public enum ResourceType
    {
        Metal,       // basic building material — used for ships and structures
        Energy,      // powers all station modules and fleet operations
        Crystals,    // rare material — required for advanced technologies
        DarkMatter   // ultra-rare — unlocks endgame content
    }
}
